package com.subterranean_security.crimson.core.ui.debug;

import java.awt.BorderLayout;

import javax.swing.JFrame;
import javax.swing.JPanel;
import javax.swing.JTabbedPane;
import javax.swing.border.EmptyBorder;

public class DebugFrame extends JFrame {

	private static final long serialVersionUID = 1L;
	private JPanel contentPane;

	public DebugFrame() {
		setTitle("Crimson - Debug");
		setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
		setBounds(100, 100, 450, 300);
		contentPane = new JPanel();
		contentPane.setBorder(new EmptyBorder(5, 5, 5, 5));
		contentPane.setLayout(new BorderLayout(0, 0));
		setContentPane(contentPane);

		JTabbedPane tabbedPane = new JTabbedPane(JTabbedPane.TOP);
		contentPane.add(tabbedPane, BorderLayout.CENTER);

		JPanel panel = new JPanel();
		tabbedPane.addTab("Database", null, panel, null);

		JPanel panel_1 = new JPanel();
		tabbedPane.addTab("Streams", null, panel_1, null);
		panel_1.setLayout(new BorderLayout(0, 0));

		panel_1.add(new StreamList());

		JPanel panel_2 = new JPanel();
		tabbedPane.addTab("Profiles", null, panel_2, null);
		panel_2.setLayout(new BorderLayout(0, 0));
		panel_2.add(new ProfileList());
	}

}
